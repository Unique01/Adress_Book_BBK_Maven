package model;

public class Adress {
	
	private String number;
	private String street;
	private String postalCode;
	private String city;

	public Adress(String number, String street, String postalCode, String city){
		this.number = number;
		this.street = street;
		this.postalCode = postalCode;
		this.city = city;
	}

}
